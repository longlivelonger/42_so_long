#include "libft.h"

const void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = -1;
	while (++i < n)
		if (*(unsigned char *)(s + i) == (unsigned char)c)
			return (s + i);
	return (0);
}
