#include "libft.h"

int	ft_atoi(const char *str)
{
	int	num;
	int	i;
	int	minus;

	minus = 0;
	num = 0;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '+' || str[i] == '-')
		if (str[i++] == '-')
			minus = 1;
	i--;
	while (str[++i] >= '0' && str[i] <= '9')
	{
		if (num * 10 < 0 && minus)
			return (0);
		if (num * 10 < 0)
			return (-1);
		num = num * 10 + str[i] - '0';
	}
	if (minus)
		return (-num);
	return (num);
}
