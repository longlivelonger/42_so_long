#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*next;

	if (lst == 0 || *lst == 0)
		return ;
	next = (*lst)->next;
	while (*lst != 0)
	{
		ft_lstdelone(*lst, del);
		*lst = next;
		if (next != 0)
			next = next->next;
	}
}
